/*
No JavaScript temos 4 tipos de números, os inteiros (por exemplo: 13), os reais ou float (por exemplo: 83.1), o Not a Number (NaN) e o infinito (Infinity).
*/

// números interios
console.log(33)
console.log(-984)

// números reais
console.log(122.034)
console.log(-0.0001)

// números inteiros e reais com expressões
console.log(33 + (-984))
console.log(122.034 / (-0.0001))
