{
    let one = 1
    let two = 2

    // == igual a [...]
    console.log(two == 1) // o resultado é `false`
    console.log(one == 1) // o resultado é `true`

    // != diferente de [...]
    console.log(one != two) // o resultado é `true`
    console.log(one != 1) // o resultado é `false`
    console.log(one != `1`) // o resultado é `false`
}